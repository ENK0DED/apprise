/// Replace :emoji_name: patterns with Unicode emoji characters.
pub fn interpret_emojis(text: &str) -> String {
  let map = emoji_map();
  let mut result = String::with_capacity(text.len());
  let mut chars = text.char_indices().peekable();
  while let Some((i, ch)) = chars.next() {
    if ch == ':' {
      // Look ahead for closing :
      let rest = &text[i + 1..];
      if let Some(end) = rest.find(':') {
        let name = &rest[..end];
        // Valid emoji names: alphanumeric, _, +, -
        if !name.is_empty() && name.chars().all(|c| c.is_alphanumeric() || c == '_' || c == '+' || c == '-') {
          if let Some(emoji) = map.get(name) {
            result.push_str(emoji);
            // Skip past the closing :
            for _ in 0..=end {
              chars.next();
            }
            continue;
          }
        }
      }
      result.push(ch);
    } else {
      result.push(ch);
    }
  }
  result
}

#[cfg(test)]
mod tests {
  use super::*;

  #[test]
  fn test_known_emoji_smile() {
    assert_eq!(interpret_emojis(":smile:"), "😄");
  }

  #[test]
  fn test_known_emoji_heart() {
    assert_eq!(interpret_emojis(":heart:"), "❤️");
  }

  #[test]
  fn test_known_emoji_thumbsup() {
    // +1 is escaped as \+1 in the map key, but the lookup is by name
    // Let's test a simpler one
    assert_eq!(interpret_emojis(":zap:"), "⚡");
  }

  #[test]
  fn test_known_emoji_100() {
    assert_eq!(interpret_emojis(":100:"), "💯");
  }

  #[test]
  fn test_unknown_emoji_left_as_is() {
    assert_eq!(interpret_emojis(":nonexistent_emoji:"), ":nonexistent_emoji:");
  }

  #[test]
  fn test_multiple_emojis() {
    let result = interpret_emojis("Hello :smile: world :zap:!");
    assert_eq!(result, "Hello 😄 world ⚡!");
  }

  #[test]
  fn test_emoji_empty_string() {
    assert_eq!(interpret_emojis(""), "");
  }

  #[test]
  fn test_no_emojis() {
    assert_eq!(interpret_emojis("Just plain text"), "Just plain text");
  }

  #[test]
  fn test_single_colon_no_match() {
    assert_eq!(interpret_emojis("time is 3:00"), "time is 3:00");
  }

  #[test]
  fn test_emoji_at_start_and_end() {
    let result = interpret_emojis(":smile: text :heart:");
    assert!(result.starts_with("😄"));
    assert!(result.ends_with("❤️"));
  }

  #[test]
  fn test_adjacent_emojis() {
    let result = interpret_emojis(":smile::zap:");
    assert_eq!(result, "😄⚡");
  }

  #[test]
  fn test_emoji_with_underscores() {
    assert_eq!(interpret_emojis(":alarm_clock:"), "⏰");
  }
}
